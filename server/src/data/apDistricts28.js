// Export functions
export function getAllDistricts() {
  return Object.keys(AP_DISTRICTS_DATA).map(name => ({
    name,
    code: AP_DISTRICTS_DATA[name].code,
    mandalCount: AP_DISTRICTS_DATA[name].mandals.length,
    coordinates: AP_DISTRICTS_DATA[name].coordinates
  }));
}

export function getMandalsForDistrict(districtName) {
  const district = AP_DISTRICTS_DATA[districtName];
  if (!district) return [];
  return district.mandals;
}

export function getMandalCoordinates(districtName, mandalName) {
  const district = AP_DISTRICTS_DATA[districtName];
  if (!district) return null;
  
  const mandal = district.mandals.find(m => m.name === mandalName);
  return mandal ? mandal.coordinates : null;
}

export function searchDistricts(query) {
  if (!query) return getAllDistricts();
  
  const lowerQuery = query.toLowerCase();
  return getAllDistricts().filter(d => 
    d.name.toLowerCase().includes(lowerQuery)
  );
}

export function searchMandalsInDistrict(districtName, query) {
  const mandals = getMandalsForDistrict(districtName);
  if (!query) return mandals;
  
  const lowerQuery = query.toLowerCase();
  return mandals.filter(m => m.name.toLowerCase().includes(lowerQuery));
}
