export const CATEGORY_KEYS = ["vegetable", "fruit", "pulse", "grain", "spice"];

const normalize = (value = "") => value.toString().trim().toLowerCase().replace(/[-_]+/g, " ").replace(/\s+/g, " ");
const unique = (values) => [...new Set(values)];

const searchCategories = (name, categories) => {
  const exactResults = [];

  categories.forEach((key) => {
    const list = CROP_VARIETIES[key]?.[name];
    if (Array.isArray(list)) {
      exactResults.push(...list);
    }
  });

  if (exactResults.length > 0) {
    return unique(exactResults);
  }

  const fuzzyResults = [];
  categories.forEach((key) => {
    Object.entries(CROP_VARIETIES[key] || {}).forEach(([cropKey, varieties]) => {
      if (cropKey.includes(name) || name.includes(cropKey)) {
        fuzzyResults.push(...varieties);
      }
    });
  });

  return unique(fuzzyResults).slice(0, 30);
};

export function getCropVarieties(cropName, category) {
  const name = normalize(cropName);
  const selectedCategory = normalize(category);

  if (!name) {
    return [];
  }

  const categoriesToSearch = CATEGORY_KEYS.filter((key) => !selectedCategory || selectedCategory === key);
  const primaryResults = searchCategories(name, categoriesToSearch);

  if (primaryResults.length > 0) {
    return primaryResults;
  }

  if (selectedCategory) {
    return searchCategories(name, CATEGORY_KEYS);
  }

  return [];
}

export function getCropCatalog(category) {
  const selectedCategory = normalize(category);
  const categoriesToSearch = CATEGORY_KEYS.filter((key) => !selectedCategory || selectedCategory === key);
  const cropNames = categoriesToSearch.flatMap((key) => Object.keys(CROP_VARIETIES[key] || {}));
  return unique(cropNames).sort((a, b) => a.localeCompare(b));
}

export function getCropCatalogSummary() {
  return CATEGORY_KEYS.map((key) => {
    const entries = Object.keys(CROP_VARIETIES[key] || {});
    const varietyCount = Object.values(CROP_VARIETIES[key] || {}).reduce((sum, values) => sum + values.length, 0);
    return {
      category: key,
      crops: entries.length,
      varieties: varietyCount,
    };
  });
}
